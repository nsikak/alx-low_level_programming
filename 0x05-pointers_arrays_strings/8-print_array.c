#include "main.h"

/**
 * rev_string - resets a pointer
 * Description: Just a function
 * Return: 0
 * @s: pointer params
 */

void rev_string(char *s)
{
	char t;
	int i = 0;
	int v  = 0;

	while (s[i] != '\0')
	{
		i++;
	}
	i--;
	while (v < i)
	{
		t = s[i];
		s[i] = s[v];
		s[v] = t;
		i--;
		v++;
	}
}
